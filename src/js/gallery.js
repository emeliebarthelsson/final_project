// get element
const galleryCardContainer = document.querySelector(".gallery-container");

// event listener to window
window.addEventListener("DOMContentLoaded", () => renderGallery(images));

// render gallery
function renderGallery(images) {
    galleryCardContainer.textContent = "";
    images.forEach(image => {
        // create elements
        const card = document.createElement("div");
        card.classList.add("gallery__card");

        const img = document.createElement("img");
        img.src = image.imageUrl;
        img.alt = image.altText;

        const cardContent = document.createElement("div");
        cardContent.classList.add("card__content");

        const watch = document.createElement("p");
        watch.textContent = `Watch: ${image.watch}`;

        const camera = document.createElement("p");
        camera.textContent = `Camera: ${image.camera}`;

        const focalLength = document.createElement("p");
        focalLength.textContent = `Focal length: ${image.focalLength}`;

        // append elements
        cardContent.append(watch, camera, focalLength);
        card.append(img, cardContent);
        galleryCardContainer.append(card);
    });
};

// back-to-top button for gallery
const toTopButton = document.querySelector(".to-top");

window.addEventListener("scroll", () => {
    if (window.scrollY > 600) {
        toTopButton.style.display = "block";
    } else {
        toTopButton.style.display ="none";
    }
});

toTopButton.addEventListener("click", () => {
    window.scrollTo({
        top: 0,
        behavior: "smooth"
    });
});